package com.company;

public class WhileLoopDemo2 {
    public static void main(String[] args) {

        int age = 5;


        if (age >=5){
            System.out.println("go to school");

        }
        while (age <=18){
            System.out.println("age: " +age+ " go to school");
            age = age+1;

        }
    }
}
