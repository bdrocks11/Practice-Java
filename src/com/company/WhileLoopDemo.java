package com.company;

public class WhileLoopDemo {
    public static void main(String[] args) {
        // will go to school from age 4 to age 18

        int age = 4;

        while (age <=18){
            System.out.println( "Age: " + age + ", will go to school ");
            age  = age +1;
            age= 19;
        }
    }
}
