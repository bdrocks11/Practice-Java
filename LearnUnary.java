class LearnUnary {
public static void main (String [] args) {

int result = +1;

// result os now 1.

System.out.println (result);

result --;
//result is now 0

System.out.println (result);

result ++;
// result is now  1

System.out.println (result);

result = -result;
// result is now -1

boolean success = false;

// false

System.out.println (success);

//true

System.out.println (!success);

}
}
